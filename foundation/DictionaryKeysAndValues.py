def printDictionary(dictionary):
    for key, value in dictionary.items():
        print(key,":",value)

#Defining a dictionary
myDictionary = {"1":"Aiswarya Mishra",
                "2":"Ira Mishra",
                "3":"Suvashree Mishra",
                "4":"Kshirod Chandra Mishra"}
printDictionary(myDictionary)